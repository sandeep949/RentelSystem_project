    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:

        if (txt_name.getText().equals("")
                || txt_phone.getText().equals("")
                || txt_email.getText().equals("")
                || txt_address.getText().equals("")
                || txt_username.getText().equals("")
                || txt_password.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "One of the fields is empty", "CRMS - Error Messga", JOptionPane.ERROR_MESSAGE);
        } else {

            query = "INSERT INTO `employee`(`name`, `gender`, `phone`, `email`, `address`, `date`, `username`, `password`) VALUES (?,?,?,?,?,?,?,?)";
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/carrentalmanagementsystem", "root", "root");
                pst = con.prepareStatement(query);

                pst.setString(1, txt_name.getText());

                String gender = "";
                if (rbtn_male.isSelected()) {
                    gender = "M";
                } else if (rbtn_female.isSelected()) {
                    gender = "F";
                }
                pst.setString(2, gender);
                pst.setString(3, txt_phone.getText());
                pst.setString(4, txt_email.getText());
                pst.setString(5, txt_address.getText());
                pst.setString(6, cr.setDate() + " " + cr.setTime());
                pst.setString(7, txt_username.getText());
                pst.setString(8, txt_password.getText());

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Employee Added Succesfully");
                getAllEmpRecords();
                clear();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex, "Car Rental Services - Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_addActionPerformed

