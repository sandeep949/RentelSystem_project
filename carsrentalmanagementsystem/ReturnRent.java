    private void tbl_custMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_custMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbl_cust.getSelectedRow();

        txt_rentalId.setText(model.getValueAt(selectedRow, 0).toString());
        txt_carId.setText(model.getValueAt(selectedRow, 1).toString());
        txt_carName.setText(model.getValueAt(selectedRow, 2).toString());
        txt_rentalDate.setText(model.getValueAt(selectedRow, 3).toString());
        txt_expectedReturnDate.setText(model.getValueAt(selectedRow, 4).toString());

        try {
            returnDate = sdf.parse(txt_returnDate.getText());
            expectedReturnDate = sdf.parse(txt_expectedReturnDate.getText());

        } catch (ParseException ex) {
            Logger.getLogger(ReturnRent.class.getName()).log(Level.SEVERE, null, ex);
        }

        Long dur = returnDate.getTime() - expectedReturnDate.getTime();
        Long duration = TimeUnit.DAYS.convert(dur, TimeUnit.MILLISECONDS);

        if (duration > 0) {
            txt_overdue.setText(Long.toString(duration));

            JOptionPane.showMessageDialog(this, "The customer has passed their expected return date");
            txt_fine.setText(Double.toString((double) fine * duration));
        } else if (duration <= 0) {
            txt_overdue.setText(Integer.toString(0));

            txt_fine.setText(Double.toString((double) fine * 0));
        }


    }//GEN-LAST:event_tbl_custMouseClicked

