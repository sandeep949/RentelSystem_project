    void getAllEmpRecords() {
        query = "SELECT * FROM `employee`";

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/carrentalmanagementsystem", "root", "root");
            pst = con.prepareStatement(query);

            rs = pst.executeQuery();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9)});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "CRMS - Error Message", JOptionPane.ERROR_MESSAGE);

        }

    }

    void getEmpRecordsById() {
        query = "SELECT * FROM `employee` WHERE `emp_id`=?";
        String id = txt_empId.getText();
        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Please input first the customer Id");
        } else {

            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/carrentalmanagementsystem", "root", "root");
                pst = con.prepareStatement(query);
                pst.setString(1, txt_empId.getText());

                rs = pst.executeQuery();
                model.setRowCount(0);

                while (rs.next()) {
                    model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9)});
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Car Rental Services - Error Message", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();

            }
        }
    }


