    void getCustomers() {
        query = "SELECT `rental_id`, `car_id`, `car_name`, `date_of_rent`, `expected_return_date`, `return_date`  FROM `rentals` WHERE `cust_id`=? and `is_returned`=?";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/carrentalmanagementsystem", "root", "root");
            pst = con.prepareStatement(query);

            pst.setString(1, txt_custId.getText());
            pst.setString(2, Integer.toString(0));

            rs = pst.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});

            }

            // JOptionPane.showMessageDialog(null, "Vehicle Rent Added Succesfully");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Car Rental Services - Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }


