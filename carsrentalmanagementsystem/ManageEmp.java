    private void tbl_empMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_empMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbl_emp.getSelectedRow();

        txt_empId1.setText(model.getValueAt(selectedRow, 0).toString());
        txt_name.setText(model.getValueAt(selectedRow, 1).toString());

        String gender = model.getValueAt(selectedRow, 2).toString();
        if (gender.equals("M")) {
            rbtn_male.setSelected(true);
            rbtn_female.setSelected(false);
        } else if (gender.equals("F")) {
            rbtn_female.setSelected(true);
            rbtn_male.setSelected(false);
        }

        txt_phone.setText(model.getValueAt(selectedRow, 3).toString());
        txt_email.setText(model.getValueAt(selectedRow, 4).toString());
        txt_address.setText(model.getValueAt(selectedRow, 5).toString());
        txt_regDate.setText(model.getValueAt(selectedRow, 6).toString());
        txt_username.setText(model.getValueAt(selectedRow, 7).toString());
        txt_password.setText(model.getValueAt(selectedRow, 8).toString());


    }//GEN-LAST:event_tbl_empMouseClicked

